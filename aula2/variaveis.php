<h2>Variáveis</h2>
<p> São estruturas (memória) utilizada
 para armazenar dados que serão processados pelo programa. </p>

 <p>
    Para criar uma variável em PHP basta colocar simbolo de $ na frente de de um nome 
    e atribuir um valor com o simboo de igual. Exemplo:

    <pre>
    $variavel = 10;
    $nome = "Maria";
    $noto = 5.5;
</pre>
</p>

<?php
    $nome = "Ivan";
    echo "Olá $nome, seja bem vindo a auala de backend!";
?>